package com.air.edward.airticket.Object;

public class Order {
	private int id;
	private String startdrome;
	private String arrivedrome;
	private String airlinecode;
	private String startdate;
	private String company;
	private String startTime;
	private String arriveTime;
	private String state;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getStartdrome() {
		return startdrome;
	}
	public void setStartdrome(String startdrome) {
		this.startdrome = startdrome;
	}
	public String getArrivedrome() {
		return arrivedrome;
	}
	public void setArrivedrome(String arrivedrome) {
		this.arrivedrome = arrivedrome;
	}
	public String getAirlinecode() {
		return airlinecode;
	}
	public void setAirlinecode(String airlinecode) {
		this.airlinecode = airlinecode;
	}
	public String getStartdate() {
		return startdate;
	}
	public void setStartdate(String startdate) {
		this.startdate = startdate;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getArriveTime() {
		return arriveTime;
	}
	public void setArriveTime(String arriveTime) {
		this.arriveTime = arriveTime;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
}
